int Date::square[13][17] = {0};
void Date::createBy(int guan) {
	if (guan == -1){
		levelDefault();
		return;
	}
	switch (guan % 8) {
	case 0:
		level1();
		break;
	case 1:
		level2();
		break;
	case 2:
		level3();
		break;
	case 3:
		level4();
		break;
	case 4:
		level5();
		break;
	case 5:
		level6();
		break;
	case 6:
		level7();
		break;
	case 7:
		level8();
		break;
	}
	for (int i = 0; i <= 5; i++) {
		for (int j = 0; j <= 16; j++) {
			int tmp = square[i][j];
			square[i][j] = square[11 - i][j];
			square[11 - i][j] = tmp;
		}
	}
}

